# RANGES

for i in range(5):
    print(i)

print("-------")

for i in range(3, 10):
    print(i)

print("-------")

for i in range(2, 20, 2):
    print(i)

menu = ['pizza', 'burger', 'biryani', 'chicken', 'eggs']

print("-------")

for n in range(len(menu)):
    print(n, menu[n])

print("-------")

for n in range(len(menu)-1, -1, -1):
    print(n, menu[n])